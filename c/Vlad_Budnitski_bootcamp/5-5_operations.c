#include <stdio.h>

int main(){
	int a = 5;
	int b = 2;

	printf("a=%d\nb=%d\na+b=%d\n",a,b,a+b);
	printf("----------\n");
	printf("a=%d\nb=%d\na-b=%d\n",a,b,a-b);
	printf("----------\n");
	printf("a=%d\nb=%d\na*b=%d\n",a,b,a*b);
	printf("----------\n");
	printf("a=%d\nb=%d\na/b=%d\n",a,b,a/b);
	printf("----------\n");
	printf("a=%d\nb=%d\na%%b=%d\n",a,b,a%b);
	return 0;
}
