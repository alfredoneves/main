<?php
// +=, -=, *=, /=, %=
$a = 10;
$b = 2;

echo "\$a = $a <br>";
echo "\$b = $b <br>";


echo "\$a += \$b = " . $a += $b;
echo "<br>";

echo "\$a *= \$b = " . $a *= $b;        // o operador de atribuição salva o valor na variáveil
echo "<br>";

echo "\$a /= \$b = " . $a /= $b;
echo "<br>";

echo "\$a %= \$b = " . $a %= $b;
echo "<br>";
?>
